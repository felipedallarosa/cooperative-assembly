CREATE TABLE VOTE
(
    ID NUMBER(10) NOT NULL,
    VOTE_SESSION_ID INTEGER NOT NULL,
    DOCUMENT VARCHAR2(14) NOT NULL,
    DECISION NUMBER(1) DEFAULT 1 NOT NULL,

    CONSTRAINT "VOTE_PK" PRIMARY KEY ("ID") ENABLE,
    CONSTRAINT VOTE_VOTE_SESSION_FK
        FOREIGN KEY ( VOTE_SESSION_ID ) REFERENCES VOTING_SESSION ( ID )
);
